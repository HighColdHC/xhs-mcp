const fs = require('fs');
const path = require('path');

// 创建一个简单的16x16红色PNG图标
// 这是一个有效的最小PNG文件，包含一个红色方块
const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR length
  0x49, 0x48, 0x44, 0x52, // IHDR type
  0x00, 0x00, 0x00, 0x10, // width: 16
  0x00, 0x00, 0x00, 0x10, // height: 16
  0x08, 0x02, 0x00, 0x00, 0x00, // bit depth: 8, color type: 2 (RGB), others: 0
  0x2C, 0xE4, 0x81, 0x63, // CRC

  0x00, 0x00, 0x00, 0x0E, // IDAT length
  0x49, 0x44, 0x41, 0x54, // IDAT type
  // RGB data: 16x16 red pixels (FF0000)
  0x78, 0x9C, 0x62, 0x00, 0x02, 0x00, 0x00, 0x05, 0x00, 0x01, 0x0D,
  0x4A, 0x8B, 0x42, // CRC

  0x00, 0x00, 0x00, 0x00, // IEND length
  0x49, 0x45, 0x4E, 0x44, // IEND type
  0xAE, 0x42, 0x60, 0x82  // CRC
]);

fs.writeFileSync(path.join(__dirname, 'icon.png'), pngData);
console.log('Created icon.png');
